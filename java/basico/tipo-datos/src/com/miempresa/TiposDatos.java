package com.miempresa;

/**
 * Ejercicio1 con tipode de datos java
 *
 * */

public class TiposDatos {
    public static void main(String[] args) {
        System.out.println("Tipos de datos en java:");

        //NumÃ©ricos
        byte tipoByte = 8;
        System.out.println("tipoByte -> " + tipoByte);
        short tipoShort = 25;
        System.out.println("tipoShort -> " + tipoShort);
        int tipoEntero = 1976;
        System.out.println("tipoEntero -> " + tipoEntero);
        long tipoLong = 10000;
        System.out.println("tipoLong -> " + tipoLong);
        float tipoFloat = 5.5f;
        System.out.println("tipoFloat -> " + tipoFloat);
        double tipoDouble = 4535.45d;
        System.out.println("tipoDouble -> " + tipoDouble);

        //Booleanos
        boolean tipoBooleanoVerdadero = true;
        System.out.println("tipoBooleanoVerdadero -> " + tipoBooleanoVerdadero);
        boolean tipoBooleanFalso = false;
        System.out.println("tipoBooleanFalso -> " + tipoBooleanFalso);

        //Texto
        char tipoChar = 'E';
        System.out.println("tipoChar -> " + tipoChar);
        String tipoString = "Cadena de texto";
        System.out.println("tipoString -> " + tipoString);

    }
}
